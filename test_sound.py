import time
import sys

def main():
    print("=== Sound Test Start ===")
    print("3秒後にMac側で音が鳴る信号を送ります...")
    
    # 3秒待つ（準備用）
    time.sleep(3)
    
    # ---------------------------------------------------------
    # これが「Macで音を鳴らす」ための魔法のコードです
    # '\a' は ASCII Bell と呼ばれ、ターミナルに「音を鳴らせ」と指示します
    # ---------------------------------------------------------
    print('\a') 
    sys.stdout.flush() # 即座に送信するためにバッファをフラッシュ
    
    print("今、'ポン'と鳴りましたか？")
    print("鳴らない場合は、Mac側のターミナル設定が必要です（後述）。")

if __name__ == "__main__":
    main()